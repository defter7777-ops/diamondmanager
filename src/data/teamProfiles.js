/**
 * Get team member profile by name or email
 */
export const getTeamMemberProfile = (identifier) => {
  const normalizedId = identifier?.toLowerCase();
  
  // Try by name first
  if (TEAM_PROFILES[normalizedId]) {
    return TEAM_PROFILES[normalizedId];
  }
  
  // Try by email
  const profileByEmail = Object.values(TEAM_PROFILES).find(
    profile => profile.email?.toLowerCase() === normalizedId
  );
  
  return profileByEmail || null;
};

/**
 * Get all team member names
 */
export const getTeamMemberNames = () => {
  return Object.keys(TEAM_PROFILES);
};

/**
 * Get team superpowers summary
 */
export const getTeamSuperpowers = () => {
  const superpowers = {};
  Object.entries(TEAM_PROFILES).forEach(([name, profile]) => {
    superpowers[name] = profile.superpowers;
  });
  return superpowers;
};